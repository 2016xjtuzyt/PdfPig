        /// ShiftJIS.
        /// </summary>
        ShiftJis = 2,
        /// <summary>
        /// PRC.
        /// </summary>
        Prc = 3,
        /// <summary>
        /// Big5.
        /// </summary>
        Big5 = 4,
        /// <summary>
        /// Wansung.
        /// </summary>
        Wansung = 5,
        /// <summary>
        /// Johab.
        /// </summary>
        Johab = 6,
        /// <summary>
        /// Reserved.
        /// </summary>
        Reserved7 = 7,
        /// <summary>
        /// Reserved.
        /// </summary>
        Reserved8 = 8,
        /// <summary>
        /// Reserved.
        /// </summary>
        Reserved9 = 9,
        /// <summary>
        /// Unicode full repertoire.
        /// </summary>
        FullUnicode = 10
    }
}